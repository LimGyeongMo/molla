package com.example.daegurobus.model;

public class RouteBookmark {
    private String routeId;
    private String routeNum;
    private String routeNum2;
    private String routeTp;
    private String startnodeName;
    private String endtnodeName;

    public String getRouteId() {
        return routeId;
    }

    public void setRouteId(String routeId) {
        this.routeId = routeId;
    }

    public String getRouteNum() {
        return routeNum;
    }

    public void setRouteNum(String routeNum) {
        this.routeNum = routeNum;
    }

    public String getRouteNum2() {
        return routeNum2;
    }

    public void setRouteNum2(String routeNum2) {
        this.routeNum2 = routeNum2;
    }

    public String getRouteTp() {
        return routeTp;
    }

    public void setRouteTp(String routeTp) {
        this.routeTp = routeTp;
    }

    public String getStartnodeName() {
        return startnodeName;
    }

    public void setStartnodeName(String startnodeName) {
        this.startnodeName = startnodeName;
    }

    public String getEndtnodeName() {
        return endtnodeName;
    }

    public void setEndtnodeName(String endtnodeName) {
        this.endtnodeName = endtnodeName;
    }
}
